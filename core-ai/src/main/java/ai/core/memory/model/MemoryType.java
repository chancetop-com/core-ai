package ai.core.memory.model;

/**
 * Memory type enumeration.
 *
 * @author xander
 */
public enum MemoryType {
    /**
     * Semantic memory: facts, preferences, knowledge about the user.
     */
    SEMANTIC,

    /**
     * Episodic memory: specific events, situations, and outcomes.
     */
    EPISODIC
}
